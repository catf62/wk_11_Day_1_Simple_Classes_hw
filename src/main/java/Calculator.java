public class Calculator {

    public Calculator() {
    }


    public int addUp(int number1, int number2) {
        return number1 + number2;
    }

    public int subtractDown(int number1, int number2) {
        return number1 - number2;
    }

    public int multiplyUp(int number1, int number2) {
        return number1 * number2;
    }

    public int divideDown(int number1, int number2) {
        return number1 / number2;
    }
}

